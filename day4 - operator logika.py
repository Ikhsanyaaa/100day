#operator logika

#operator logika and akan bernilai True jika kedua ruas memenuhi syarat atau bernilai benar dan akan bernilai False jika salah satunya salah
# contoh logika and 
x=10
print(x>7 and x<11)
 
#True

#logika or akan bernilai True jika salah satu ruas memenuhi syarat atay bernilai benar dan akan bernilai False jika dua-duanya benar
# contoh logika or
x=10
print(x>7 or x<11)
 
#True

#logika not akan berkebalikan dari nilai yang ditentukan, jika syarat yang diberikan benar, maka akan bernilai False, dan sebaliknya akan bernilai True jika syarat yang diberikan salah
# contoh logika not
x=10
print(not(x>7 or x<11))
 
#False