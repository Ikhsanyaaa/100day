#operator logika
a = 5


#logika and
#logika and akan bernilai True jika kedua syarat terpenuhi
#dan bernilai false jika salah satunya tidak terpenuhi
print("apakah a = 5 dan a > 4 : ",a == 5 and a > 4)
print(a == 4 and a > 6)
print(a == 5 and a > 6)


#logika or 
#logika or akan bernilai True jika salah satu syarat terpenuhi
#dan bernilai False jika keduanya tidak terpenuhi
print(a == 5 or a < 4)
print(a == 4 or a < 4)